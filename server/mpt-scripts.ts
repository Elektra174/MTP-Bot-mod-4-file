export function getScriptById(id: string): MPTScript | undefined {
  return MPT_SCRIPTS.find(script => script.id === id);
}

export function findScriptsForContext(keywords: string[]): MPTScript[] {
  const lowerKeywords = keywords.map(k => k.toLowerCase());
  return MPT_SCRIPTS.filter(script => 
    script.applicableFor.some(context => 
      lowerKeywords.some(keyword => context.toLowerCase().includes(keyword))
    )
  );
}

