export function generateScriptGuidance(script: MPTScript): string {
  let guidance = `\n\n## ВЫБРАННЫЙ СКРИПТ: "${script.name}"\n`;
  guidance += `${script.description}\n\n`;
  
  guidance += `### СТРОГАЯ ПОСЛЕДОВАТЕЛЬНОСТЬ ЭТАПОВ:\n`;
  guidance += `**НЕЛЬЗЯ ПЕРЕСКАКИВАТЬ ЭТАПЫ!** Ты ОБЯЗАН проходить этапы скрипта СТРОГО ПО ПОРЯДКУ:\n`;
  const stageNumbers = script.stages.map((_, i) => i + 1).join(' → ');
  guidance += `${stageNumbers}\n\n`;
  
  guidance += `### ЭТАПЫ СКРИПТА:\n\n`;
  
  for (let i = 0; i < script.stages.length; i++) {
    const stage = script.stages[i];
    guidance += `#### ${stage.name}\n`;
    guidance += `**Цель этапа:** ${stage.goal}\n`;
    guidance += `**Примеры вопросов (адаптируй к контексту беседы):**\n`;
    for (const question of stage.questions) {
      guidance += `- ${question}\n`;
    }
    if (stage.transitionCriteria) {
      guidance += `\n**НЕ ПЕРЕХОДИ К СЛЕДУЮЩЕМУ ЭТАПУ, пока:** ${stage.transitionCriteria}\n`;
    }
    guidance += `\n`;
  }
  
  return guidance;
}
